 

public class while_loop{
    public static void main(String a[]){
        int i = 1;
        while(i<10){
            System.out.print("hello " + i + "\n");
            int j =1;
            while(j<5){
                System.out.print("hi " + j + "\n");
                j++;
            }
            i++;
        }

    }
};